package com.example.blogposting.car;

/**
 * The interface Car.
 */
public interface Car {

    /**
     * Car name.
     */
    public String getCarName();

    /**
     * Engine start.
     */
    public void engineStart();

    /**
     * Drive.
     */
    public void drive();

    /**
     * Engine stop.
     */
    public void engineStop();
}